const id = 100;

// Equal to ==
if(id == 100){
  console.log('CORRECT');
} else {
  console.log('INCORRECT');
}

// Not equal to !=
if(id != 101){
  console.log('CORRECT');
} else {
  console.log('INCORRECT');
}

// Equal to value AND type ===
if(id === 100){
  console.log('CORRECT');
} else {
  console.log('INCORRECT');
}

// Not equal to value and type !==
if(id !== 100){
  console.log('CORRECT');
} else {
  console.log('INCORRECT');
}